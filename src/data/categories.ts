import type { Category, CategoryId } from '../types'

export function assertUniqueWords(words: string[], categoryId: string): void {
  const lower = words.map((w) => w.toLowerCase())
  const unique = new Set(lower)
  if (unique.size !== lower.length) {
    const seen = new Set<string>()
    const dupes = lower.filter((w) => {
      if (seen.has(w)) return true
      seen.add(w)
      return false
    })
    throw new Error(`Duplicate words in category "${categoryId}": ${[...new Set(dupes)].join(', ')}`)
  }
}

export function assertNoCrossCategoryDuplicates(categories: Category[]): void {
  const seen = new Map<string, string>()
  for (const { id, words } of categories) {
    for (const word of words) {
      const key = word.toLowerCase()
      const prior = seen.get(key)
      if (prior !== undefined) {
        throw new Error(`Word "${word}" appears in both "${prior}" and "${id}"`)
      }
      seen.set(key, id)
    }
  }
}

